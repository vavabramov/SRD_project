CREATE DATABASE IF NOT EXISTS `ELECTRONICS_SHOP` DEFAULT CHARACTER SET = 'utf8' DEFAULT COLLATE 'utf8_general_ci'; 

 USE ELECTRONICS_SHOP;

CREATE TABLE IF NOT EXISTS `order_status` (
	`ID` INTEGER NOT NULL,
	`NAME` VARCHAR(64) DEFAULT NULL,
	PRIMARY KEY (`ID`)
);

CREATE TABLE IF NOT EXISTS `product` (
	`ID` INTEGER NOT NULL,
	`SKU_NAME` VARCHAR(64) DEFAULT NULL,
	`PRICE` DECIMAL(6,0) DEFAULT NULL,
	`STOCK` INTEGER DEFAULT NULL,
	`CATEGORY_ID` INTEGER NOT NULL,
	`DESCRIPTION` VARCHAR(64) DEFAULT NULL,
	PRIMARY KEY (`ID`)
);

CREATE TABLE IF NOT EXISTS `payments` (
	`ID` INTEGER NOT NULL,
	`DATE` date DEFAULT NULL,
	`METHOD` VARCHAR(64) DEFAULT NULL,
	`WAY` VARCHAR(64) DEFAULT NULL,
	`STATUS_ID` INTEGER NOT NULL,
	PRIMARY KEY (`ID`)
);

CREATE TABLE IF NOT EXISTS `clients` (
	`ID` INTEGER NOT NULL,
	`FIRST_NAME` VARCHAR(64) DEFAULT NULL,
	`LAST_NAME` VARCHAR(64) DEFAULT NULL,
	`SEX` VARCHAR(64) DEFAULT NULL,
	`BIRTH_YEAR` INTEGER DEFAULT NULL,
	`PHONE_NUMBER` INTEGER DEFAULT NULL,
	`EMAIL` VARCHAR(64) DEFAULT NULL,
	PRIMARY KEY (`ID`)
);

CREATE TABLE IF NOT EXISTS `address` (
	`ID` INTEGER NOT NULL,
	`COUNTRY` VARCHAR(64) DEFAULT NULL,
	`CITY` VARCHAR(64) DEFAULT NULL,
	`STREET` VARCHAR(64) DEFAULT NULL,
	`BUILDING` INTEGER DEFAULT NULL,
	`APPARTMENT` DECIMAL(6,0) DEFAULT NULL,
	`POSTCODE` VARCHAR(64) DEFAULT NULL,
	`CUSTOMER_ID` INTEGER NOT NULL,
	PRIMARY KEY (`ID`)
);

CREATE TABLE IF NOT EXISTS `orders` (
	`ID` INTEGER NOT NULL,
	`DATE` date DEFAULT NULL,
	`DEADLINE_DATE` date DEFAULT NULL,
	`STATUS_ID` INTEGER NOT NULL,
	`PAYMENT_ID` INTEGER NOT NULL,
	`CLIENT_ID` INTEGER NOT NULL,
	PRIMARY KEY (`ID`)
);



